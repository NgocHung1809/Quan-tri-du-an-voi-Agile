    void cbx_DSP() {
        DefaultComboBoxModel _DefaultComboBoxModel = new DefaultComboBoxModel();
        ResultSet rs = DBConnect.getSelectDataFromDB("SELECT * FROM DONGSANPHAM");
        try {
            while (rs.next()) {
                _DefaultComboBoxModel.addElement(rs.getString(2));
            }
            cbx_MaDSP.setModel(_DefaultComboBoxModel);
        } catch (SQLException ex) {
//            Logger.getLogger(DongSanPhamService.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

    String getMaDSP(int id) {
        ResultSet rs = DBConnect.getSelectDataFromDB("SELECT * FROM DONGSANPHAM");
        try {
            while (rs.next()) {
                if (id == rs.getInt(1)) {
                    return rs.getString(2);
                }
            }
        } catch (SQLException ex) {
        }
        return "false";
    }

    int getIDDSP(String ma) {
        ResultSet rs = DBConnect.getSelectDataFromDB("SELECT * FROM DONGSANPHAM");
        try {
            while (rs.next()) {
                if (ma.equalsIgnoreCase(rs.getString(2))) {
                    return rs.getInt(1);
                }
            }
        } catch (SQLException ex) {
        }
        return -1;
    }

    void loadData(List<SanPham> data) {
        _DefaultTableModel = (DefaultTableModel) tbl_SanPham.getModel();
        int stt = 0;
        _DefaultTableModel.setColumnIdentifiers(new Object[]{"STT", "Mã", "Tên", "Năm bảo hành", "Trọng lượng", "Số lượng tồn", "Giá nhập", "Giá bán", "Mô tả", "Mã DSP"});
        if (data.isEmpty()) {
            _DefaultTableModel.setRowCount(0);
            return;
        }
        _DefaultTableModel.setRowCount(0);
        for (SanPham x : data) {
            _DefaultTableModel.addRow(new Object[]{++stt, x.getMa(), x.getTen(), x.getNamBaoHanh(), x.getTrongLuongSP(), x.getSLSanPhamTon(), x.getGiaNhapSP(), x.getGiaBanSP(), x.getMoTaSP(), getMaDSP(x.getIdDSP())});
        }
    }

