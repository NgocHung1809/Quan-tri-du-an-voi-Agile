    private void btn_SuaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_SuaActionPerformed
        int count =0;
        if(txt_Ten.getText().isEmpty()||txt_NamBaoHanh.getText().isEmpty() ||txt_TrongLuong.getText().isEmpty()||txt_Ma.getText().isEmpty() || txt_SoLuongSanPhanTon.getText().isEmpty() || txt_GiaBan.getText().isEmpty()||txt_GiaBan.getText().isEmpty() ){
            JOptionPane.showMessageDialog(this, "Dữ liệu không được null và mã không được trùng");
            count++;
            return;
        }
        
        if(_uUtilities.checkSo(txt_Ten.getText()) || !_uUtilities.checkSo(txt_NamBaoHanh.getText()) || !_uUtilities.checkSo(txt_GiaBan.getText())|| !_uUtilities.checkSo(txt_GiaNhap.getText())
                || !_uUtilities.checkSo(txt_SoLuongSanPhanTon.getText()) || !_uUtilities.checkSo(txt_TrongLuong.getText())){
            JOptionPane.showMessageDialog(this, "Năm, giá, số lượng là số");
            count++;
            return;
        }
        if(count ==0){
        int choose = JOptionPane.showConfirmDialog(this, "Bạn có muốn sửa không?", "Sửa", JOptionPane.YES_NO_OPTION);
        if (choose != 0) {
            return;
        }
        JOptionPane.showMessageDialog(this, _spv.getEditData(getDataGUI(), "UPDATE"));
        loadData(_spv.getlst());
        }
    }//GEN-LAST:event_btn_SuaActionPerformed

